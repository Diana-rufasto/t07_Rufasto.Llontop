#Ejercicio01
import os
msg=os.sys.argv[1]
for letra in msg:
    if(letra=="&"):
        print("llamame ahorita")
    if(letra=="#"):
        print("llamame en 5 min")
    if(letra=="@"):
        print("ya no me llames")
    if(letra=="/"):
        print("donde estas")
#fin_for


